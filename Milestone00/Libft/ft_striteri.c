#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	size_t	i;

	i = 0;
	if (s == NULL)
		return (NULL);
	while (s[i] != '\0')
	{
		s[i] = f(i, s[i]);
		i++;
	}
	s[i] = '\0';
	return (s);
}
